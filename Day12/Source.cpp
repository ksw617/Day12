void MoveCursorPosition(int x, int y)
{
	COORD pos;
	pos.X = x * 2;
	pos.Y = y;

	SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), pos);
}


int main()
{
	id = LOGO;
	LogoInitialize();

	while (true)
	{
		system("cls");

		switch (id)
		{
		case LOGO:
			LogoProgress();
			LogoRender();
			break;
		case MENU:
			printf("MENU\n");
			break;
		case STAGE:
			printf("STAGE\n");
			break;
		default:
			break;
		}

		Sleep(50);
	}
}

